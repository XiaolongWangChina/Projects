#ifndef	_PUB_TOOL_H_
#define	_PUB_TOOL_H_
#include "71x_lib.h"

/**********************************************************************
函数名称:   Dec_2_BCD
功能:       将十进制数据转换为 BCD 码
**********************************************************************/
u8	Dec_2_BCD(u8  dec);

/**********************************************************************
函数名称:   BCD_2_Dec
功能:       将BCD码转换为 十进制数据
**********************************************************************/
u8	BCD_2_Dec(u8 bcd);

/**********************************************************************
函数名称:   BCD_Inc
功能:       将BCD码加1  (假设参数 bcd 不超过0x99)
**********************************************************************/
u8	BCD_Inc(u8 bcd);

#endif
