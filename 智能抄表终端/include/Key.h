#ifndef __KEY_H__
#define __KEY_H__

#include "71x_lib.h"
#define	KEY_PROGRAMABLE		(1UL<<1)	//编程允许键	P1.1
#define	KEY_DOWN		(1UL<<0)	//向下键    P1.0
#define	KEY_UP			(1UL<<15)	//向上键    P0.15
#define	KEY_PRESSED_TIME	0X04	        //当一个按键连续被检测到了4次

/**************************************************************************
函数名 		: Key_Init() 
功能描述	: 对按键所用到的I/O口进行初始化
**************************************************************************/
void 	Key_Init(void);	

/**************************************************************************
函数名 		: Key_Detect() 
功能描述	: 对按键进行检测
**************************************************************************/
void		Key_Detect();

/**************************************************************************
函数名 		: Process_Key_Up() 
功能描述	: 对上行键进行处理
**************************************************************************/
void		Process_Key_Up();

/**************************************************************************
函数名 		: Process_Key_Down() 
功能描述	: 对下行键进行处理
**************************************************************************/
void		Process_Key_Down();

#endif
